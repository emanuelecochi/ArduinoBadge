

public class FileProblemException extends Exception {

	private static final long serialVersionUID = -3004062423638869932L;

	public FileProblemException() {
		super();
	}

	public FileProblemException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FileProblemException(String message, Throwable cause) {
		super(message, cause);
	}

	public FileProblemException(String message) {
		super(message);
	}

	public FileProblemException(Throwable cause) {
		super(cause);
	}

}
